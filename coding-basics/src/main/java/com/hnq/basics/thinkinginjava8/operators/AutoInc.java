package com.hnq.basics.thinkinginjava8.operators;

/**
 * 递增和递减
 *
 * @author henengqiang
 * @date 2020/11/07
 */
public class AutoInc {

    public static void main(String[] args) {
        int i = 1;
        System.out.println("i=" + i);
        System.out.println("++i=" + ++i);
        System.out.println("i++=" + i++);
        System.out.println("i=" + i);
        System.out.println("--i=" + --i);
        System.out.println("i--=" + i--);
        System.out.println("i=" + i);
    }

}
